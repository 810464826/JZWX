package com.jz.jzcore.model;

public class AccountsCancel {

	private String AcctionTime;
	private String AcctionUser;
	private String PrizeNumber;
	private String productName;
	
	
	public String getAcctionTime() {
		return AcctionTime;
	}
	public void setAcctionTime(String acctionTime) {
		AcctionTime = acctionTime;
	}
	public String getAcctionUser() {
		return AcctionUser;
	}
	public void setAcctionUser(String acctionUser) {
		AcctionUser = acctionUser;
	}
	public String getPrizeNumber() {
		return PrizeNumber;
	}
	public void setPrizeNumber(String prizeNumber) {
		PrizeNumber = prizeNumber;
	}
	public String getProductName() {
		return productName;
	}
	public void setProductName(String productName) {
		this.productName = productName;
	}

}
